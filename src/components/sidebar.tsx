export async function Sidebar() {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();

  return (
    <aside className="fixed left-0 top-0 z-40 h-screen w-60 border-r border-slate-200 bg-slate-50 flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-slate-200">
        <Link href="/" className="flex items-center gap-2">
          <span className="text-lg font-semibold text-slate-900">
            AdOrchestrator
          </span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 overflow-y-auto">
        {navSections.map((section, index) => (
          <div key={section.title} className={index > 0 ? "mt-6" : ""}>
            <p className="px-3 mb-2 text-xs font-medium text-slate-400 uppercase tracking-wider">
              {section.title}
            </p>
            <div className="space-y-1">
              {section.items.map((item) => (
                <NavLink key={item.name} item={item} />
              ))}
            </div>
          </div>
        ))}
      </nav>

      {/* User Nav at bottom */}
      {user?.email && <UserNav email={user.email} />}
    </aside>
  );
}

function NavLink({ item }: { item: NavItem }) {
  return (
    <Link
      href={item.href}
      className="flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium text-slate-600 hover:text-slate-900 hover:bg-white transition-colors"
    >
      {getIcon(item.icon)}
      <span>{item.name}</span>
    </Link>
  );
}
